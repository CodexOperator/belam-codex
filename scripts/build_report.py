#!/usr/bin/env python3
"""
Build LaTeX report from local experiment analysis.

Reads the analysis markdown, converts to LaTeX with proper formatting,
embeds images, and compiles to PDF. Output lives in the pipeline's
local_results/{version}/ directory.

Two modes:
  1. Auto mode (default): Converts analysis.md → LaTeX → PDF using pandoc
  2. Agent mode: Spawns builder agent with reasoning to write custom LaTeX

Usage:
    python3 scripts/build_report.py <version>                     # Auto: pandoc MD→PDF
    python3 scripts/build_report.py <version> --agent             # Agent: builder writes LaTeX
    python3 scripts/build_report.py <version> --compile-only      # Just run pdflatex on existing .tex
    python3 scripts/build_report.py <version> --template custom   # Use custom LaTeX template
"""

